const DIMENSION_KEYS = ['contrast', 'alignment', 'repetition', 'proximity'];

function buildQuestions() {
  const list = [];
  for (const dimension of DIMENSION_KEYS) {
    const copies = QUESTION_COPY[dimension];
    const rule = DIMENSION_RULES[dimension];
    const principle = TRAINING_DIMENSIONS[dimension];
    for (let index = 0; index < copies.length; index += 1) {
      const [title, question, correct, wrong, tags] = copies[index];
      const number = String(index + 1).padStart(3, '0');
      const scenario = SCENARIOS[index];
      // index 偶数时正确项为 b，奇数时为 a，增加不可预测性
      const goodOption = index % 2 === 0 ? 'b' : 'a';
      const difficulty = index % 3 === 0 ? 'beginner' : index % 3 === 1 ? 'medium' : 'comprehensive';
      list.push({
        id: `${dimension}-${number}`,
        mode: 'good-bad-compare',
        dimension,
        scenario,
        layoutIndex: index % 10,
        title,
        question,
        options: [
          { id: 'a', label: 'A' },
          { id: 'b', label: 'B' },
        ],
        goodOption,
        answer: goodOption,
        principle,
        difficulty,
        explanation: {
          correct,
          wrong,
          rule,
        },
        tags,
      });
    }
  }
  return list;
}

export const questions = buildQuestions();

export function getQuestionById(id) {
  if (!id || typeof id !== 'string') return null;
  return questions.find((question) => question.id === id) || null;
}

export function getQuestionsByDimension(dimension) {
  if (!dimension || typeof dimension !== 'string') return [];
  return questions.filter((question) => question.dimension === dimension);
}

export function getQuestionsByDifficulty(difficulty) {
  if (!difficulty || typeof difficulty !== 'string') return [];
  return questions.filter((question) => question.difficulty === difficulty);
}

export function getTotalQuestionCount() {
  return questions.length;
}

export function validateQuestions() {
  const issues = [];
  const ids = new Set();

  if (questions.length !== 80) {
    issues.push(`题目总数应为 80，实际为 ${questions.length}`);
  }

  for (const dimension of Object.keys(TRAINING_DIMENSIONS)) {
    const count = questions.filter((question) => question.dimension === dimension).length;
    if (count !== 20) {
      issues.push(`${dimension} 题目数量应为 20，实际为 ${count}`);
    }
  }

  for (const question of questions) {
    if (ids.has(question.id)) issues.push(`题目 ID 重复: ${question.id}`);
    ids.add(question.id);

    if (!question.scenario) issues.push(`题目缺少场景: ${question.id}`);
    if (!question.options?.some((option) => option.id === question.answer)) {
      issues.push(`答案不存在于选项中: ${question.id}`);
    }
    if (!question.explanation?.correct || !question.explanation?.wrong || !question.explanation?.rule) {
      issues.push(`解析不完整: ${question.id}`);
    }
    if (typeof question.layoutIndex !== 'number' || question.layoutIndex < 0 || question.layoutIndex > 9) {
      issues.push(`layoutIndex 越界: ${question.id}`);
    }
  }

  return issues;
}
